import turtle
turtle.Screen().bgcolor("blue")
turtle.Screen().setup(300,400)
shape = turtle.Turtle()

shape.forward(100)
shape.right(120)
shape.forward(100)
shape.right(120)
shape.forward(100)
shape.penup()
shape.goto(50,30)
shape.pendown()
shape.right(180)
shape.forward(100)
shape.right(120)
shape.forward(100)
shape.right(120)
shape.forward(100)
turtle.done()